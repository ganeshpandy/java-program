class switch1//switch statement
{
public static void main(String[]args)
{
	int choice=1;
	switch(choice)
	{
		case 1:System.out.println("tamil");
		break;
		case 2:System.out.println("english");
		break;
		case 3:System.out.println("hindi");
		break;
		case 4:System.out.println("other lanaguage");
		break;
		default :System.out.println("invalid choice");
	}
	
}
}