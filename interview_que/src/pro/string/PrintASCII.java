package pro.string;

public class PrintASCII {
public static void main(String[] args) {
	char ch1 = 'a';  
	char ch2 = 'b';    
	int ascii1 = ch1;  
	int ascii2 = ch2;  
	System.out.println("The ASCII value of " + ch1 + " is: " + ascii1);  
	System.out.println("The ASCII value of " + ch2 + " is: " + ascii2);  
	
}
}
